uint8_t CalCRC8(uint8_t package[], uint8_t len)
{
	uint8_t crc = 0;
	uint16_t i;
	for (i = 0; i < len; i++)
	{
		crc = CrcTable[(crc ^ package[i]) & 0xff];
	}
	return crc;
}

